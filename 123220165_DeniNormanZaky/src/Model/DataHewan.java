
package Model;


public class DataHewan {
    public int id;
    public String nama;
    public String jenis_hewan;
    public String pemilik;
    public int nomor_telepon;
    public int durasi;
    public int total_tagihan;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getJenis_hewan() {
        return jenis_hewan;
    }

    public void setJenis_hewan(String jenis_hewan) {
        this.jenis_hewan = jenis_hewan;
    }

    public String getPemilik() {
        return pemilik;
    }

    public void setPemilik(String pemilik) {
        this.pemilik = pemilik;
    }

    public int getNomor_telepon() {
        return nomor_telepon;
    }

    public void setNomor_telepon(int nomor_telepon) {
        this.nomor_telepon = nomor_telepon;
    }

    public int getDurasi() {
        return durasi;
    }

    public void setDurasi(int durasi) {
        this.durasi = durasi;
    }

    public int getTotal_tagihan() {
        return total_tagihan;
    }

    public void setTotal_tagihan(int total_tagihan) {
        this.total_tagihan = total_tagihan;
    }
    
    
   
}
